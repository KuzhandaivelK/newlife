-- V1: Create Insurance Policy Table (Oracle DB)
CREATE TABLE POLICY (
    POLICY_ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    POLICY_NUMBER VARCHAR2(50) UNIQUE NOT NULL,
    INSURED_NAME VARCHAR2(100) NOT NULL,
    PREMIUM_AMOUNT NUMBER(10, 2) NOT NULL,
    STATUS VARCHAR2(20) DEFAULT 'PENDING' CHECK (STATUS IN ('PENDING', 'ACTIVE', 'CANCELLED', 'EXPIRED')),
    START_DATE DATE,
    END_DATE DATE,
    CREATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    UPDATED_AT TIMESTAMP
);

